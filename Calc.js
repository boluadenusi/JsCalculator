let calculatorAlert = alert ("Welcome to Js Calculator");
let calculatorPrompt = prompt ('What operation would you like to perform today? "+,-,/,*"');

if (calculatorPrompt === "+"){
let calculatorPrompt1 = parseInt(prompt ('Enter first number'));
let calculatorPrompt2 = parseInt(prompt ('Enter second number'));
let result = alert (calculatorPrompt1 + calculatorPrompt2)
}
else if(calculatorPrompt === "-"){
    let calculatorPrompt1 = parseInt(prompt ('Enter first number'));
    let calculatorPrompt2 = parseInt(prompt ('Enter second number'));
    let result = alert (calculatorPrompt1 - calculatorPrompt2)
    }
    else if(calculatorPrompt === "*"){
        let calculatorPrompt1 = parseInt(prompt ('Enter first number'));
        let calculatorPrompt2 = parseInt(prompt ('Enter second number'));
        let result = alert (calculatorPrompt1 * calculatorPrompt2)
        }
        else if(calculatorPrompt === "/"){
            let calculatorPrompt1 = parseInt(prompt ('Enter first number'));
            let calculatorPrompt2 = parseInt(prompt ('Enter second number'));
            let result = alert (calculatorPrompt1 / calculatorPrompt2)
            }







    
